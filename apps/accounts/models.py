class BarmhaUserManager(UserManager):
    """
    Custom manager that removes 'username' from create_user / create_superuser
    signatures.  Username is auto-generated by User.save().
    """

    def _auto_username(self, email):
        base    = (email or "").split("@")[0][:20].lower() or "user"
        slug    = base
        counter = 1
        while self.filter(username=slug).exists():
            slug = f"{base}{counter}"
            counter += 1
        return slug

    def create_user(self, email, password=None, **extra_fields):
        if not email:
            raise ValueError("Email address is required.")
        email = self.normalize_email(email)
        extra_fields.setdefault("is_staff", False)
        extra_fields.setdefault("is_superuser", False)
        # Auto-generate username if not supplied
        if not extra_fields.get("username"):
            extra_fields["username"] = self._auto_username(email)
        user = self.model(email=email, **extra_fields)
        user.set_password(password)
        user.save(using=self._db)
        return user

    def create_superuser(self, email, password=None, **extra_fields):
        extra_fields.setdefault("is_staff", True)
        extra_fields.setdefault("is_superuser", True)
        if not extra_fields.get("username"):
            extra_fields["username"] = self._auto_username(email)
        if extra_fields.get("is_staff") is not True:
            raise ValueError("Superuser must have is_staff=True.")
        if extra_fields.get("is_superuser") is not True:
            raise ValueError("Superuser must have is_superuser=True.")
        return self.create_user(email, password, **extra_fields)


class User(AbstractUser):
    """
    Custom user model.

    Authentication:  email (USERNAME_FIELD) or phone via LoginView.
    Username:        auto-generated from the email local part; never shown to
                     end-users but kept in the DB for Django internals / admin.
    Social auth:     handled by allauth + our own GoogleLogin / FacebookLogin
                     views that skip the username requirement entirely.
    """

    USER_TYPES = [
        ("individual",  "Individual"),
        ("agent",       "Real Estate Agent"),
        ("agency",      "Real Estate Agency"),
        ("company",     "Company / Employer"),
        ("freelancer",  "Freelancer"),
        ("merchant",    "Merchant / Business"),
    ]

    # ── Custom manager ─────────────────────────────────────────────────────────
    objects = BarmhaUserManager()

    # ── Make email the primary identifier ─────────────────────────────────────
    email             = models.EmailField(unique=True)          # override blank default
    username          = models.CharField(                       # kept for internals only
        max_length=150, unique=True,
        blank=True,                                             # never required from users
        help_text="Auto-generated internal identifier.",
    )
    USERNAME_FIELD    = "email"
    REQUIRED_FIELDS   = []                                      # no extra prompts for createsuperuser

    # ── Profile fields ─────────────────────────────────────────────────────────
    user_type         = models.CharField(max_length=20, choices=USER_TYPES, default="individual")
    phone             = models.CharField(max_length=20, blank=True)
    whatsapp          = models.CharField(max_length=20, blank=True)
    avatar            = ProcessedImageField(
        upload_to="avatars/",
        processors=[Transpose(), ResizeToFill(400, 400)],
        format="WEBP", options={"quality": 80},
        blank=True, null=True,
        validators=[validate_image_size, validate_image_type],
    )
    avatar_thumbnail  = ImageSpecField(source="avatar", id="core:avatar")
    bio               = models.TextField(blank=True)
    is_verified       = models.BooleanField(default=False)
    language          = models.CharField(max_length=5, default="ar")
    social_avatar_url = models.URLField(blank=True)
    created_at        = models.DateTimeField(auto_now_add=True)
    updated_at        = models.DateTimeField(auto_now=True)

    class Meta:
        db_table = "users"

    # ── Auto-generate username from email ──────────────────────────────────────
    def save(self, *args, **kwargs):
        if not self.username:
            base    = (self.email or "").split("@")[0][:20].lower() or "user"
            slug    = base
            counter = 1
            while User.objects.filter(username=slug).exclude(pk=self.pk).exists():
                slug = f"{base}{counter}"
                counter += 1
            self.username = slug
        super().save(*args, **kwargs)


