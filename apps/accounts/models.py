class PhoneVerification(models.Model):
    user       = models.ForeignKey(User, on_delete=models.CASCADE, related_name="otp_requests")
    phone      = models.CharField(max_length=20)
    code       = models.CharField(max_length=6)
    is_used    = models.BooleanField(default=False)
    attempts   = models.PositiveSmallIntegerField(default=0)
    expires_at = models.DateTimeField()
    created_at = models.DateTimeField(auto_now_add=True)

    @classmethod
    def create_for_user(cls, user, phone):
        code = "".join(random.choices(string.digits, k=6))
        return cls.objects.create(
            user=user, phone=phone, code=code,
            expires_at=timezone.now() + timedelta(minutes=10),
        )

    def is_valid(self, submitted_code):
        if self.is_used:
            return False, "already_used"
        if self.attempts >= 5:
            return False, "too_many_attempts"
        if timezone.now() > self.expires_at:
            return False, "expired"
        self.attempts += 1
        self.save(update_fields=["attempts"])
        if self.code != submitted_code:
            return False, "invalid_code"
        self.is_used = True
        self.save(update_fields=["is_used"])
        return True, "ok"

    def __str__(self):
        return f"{self.user} - {self.phone}"


