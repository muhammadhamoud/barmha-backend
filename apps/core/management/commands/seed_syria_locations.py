XLSX_NS = {"a": "http://schemas.openxmlformats.org/spreadsheetml/2006/main"}
RELS_NS = {"r": "http://schemas.openxmlformats.org/package/2006/relationships"}
OFFICE_REL_NS = "http://schemas.openxmlformats.org/officeDocument/2006/relationships"


@dataclass(frozen=True)
class LocationPayload:
    governorate_slug: str
    slug: str
    location_type: str
    city_en: str
    name_en: str
    city_ar: str
    name_ar: str
    latitude: Optional[Decimal]
    longitude: Optional[Decimal]
    is_popular: bool


def clean(value) -> str:
    value = str(value or "").strip()
    if value.lower() in {"", "none", "null", "<null>", "nan", "-"}:
        return ""
    return re.sub(r"\s+", " ", value)


def parse_decimal(value) -> Optional[Decimal]:
    value = clean(value)
    if not value:
        return None
    try:
        return Decimal(value)
    except (InvalidOperation, ValueError):
        return None


def make_slug(*parts: str) -> str:
    source = "-".join(clean(part) for part in parts if clean(part))
    value = slugify(source, allow_unicode=False) or slugify(source, allow_unicode=True)
    return value[:140]


def is_popular(*names: str) -> bool:
    return any(clean(name).lower() in POPULAR_PLACE_KEYS for name in names if clean(name))


class SimpleXlsxReader:
    """Small XLSX reader for values-only worksheets generated by HDX/OCHA."""

    def __init__(self, path: Path):
        self.path = path
        self.shared_strings: List[str] = []
        self.sheet_targets: Dict[str, str] = {}

    def __enter__(self):
        self.zip_file = ZipFile(self.path)
        self.shared_strings = self._load_shared_strings()
        self.sheet_targets = self._load_sheet_targets()
        return self

    def __exit__(self, exc_type, exc, tb):
        self.zip_file.close()

    def _load_shared_strings(self) -> List[str]:
        if "xl/sharedStrings.xml" not in self.zip_file.namelist():
            return []

        root = ET.fromstring(self.zip_file.read("xl/sharedStrings.xml"))
        strings = []
        for item in root.findall("a:si", XLSX_NS):
            strings.append("".join(t.text or "" for t in item.findall(".//a:t", XLSX_NS)))
        return strings

    def _load_sheet_targets(self) -> Dict[str, str]:
        rels_root = ET.fromstring(self.zip_file.read("xl/_rels/workbook.xml.rels"))
        rels = {
            rel.attrib["Id"]: rel.attrib["Target"].lstrip("/")
            for rel in rels_root
        }

        workbook_root = ET.fromstring(self.zip_file.read("xl/workbook.xml"))
        targets = {}
        for sheet in workbook_root.findall("a:sheets/a:sheet", XLSX_NS):
            name = sheet.attrib["name"]
            rel_id = sheet.attrib[f"{{{OFFICE_REL_NS}}}id"]
            target = rels[rel_id]
            if not target.startswith("xl/"):
                target = "xl/" + target
            targets[name] = target
        return targets

    @staticmethod
    def _column_index(cell_reference: str) -> int:
        letters = "".join(ch for ch in cell_reference if ch.isalpha())
        index = 0
        for ch in letters.upper():
            index = index * 26 + ord(ch) - 64
        return index - 1

    def _cell_value(self, cell) -> str:
        value_node = cell.find("a:v", XLSX_NS)
        if value_node is None:
            return ""

        value = value_node.text or ""
        if cell.attrib.get("t") == "s" and value:
            return self.shared_strings[int(value)]
        return value

    def rows(self, sheet_name: str) -> Iterable[Dict[str, str]]:
        if sheet_name not in self.sheet_targets:
            return []

        root = ET.fromstring(self.zip_file.read(self.sheet_targets[sheet_name]))
        parsed_rows: List[List[str]] = []

        for row in root.findall("a:sheetData/a:row", XLSX_NS):
            values: List[str] = []
            for cell in row.findall("a:c", XLSX_NS):
                idx = self._column_index(cell.attrib.get("r", "A1"))
                while len(values) <= idx:
                    values.append("")
                values[idx] = self._cell_value(cell)
            parsed_rows.append(values)

        if not parsed_rows:
            return []

        headers = [clean(header) for header in parsed_rows[0]]
        result = []
        for row in parsed_rows[1:]:
            if not any(clean(value) for value in row):
                continue
            padded = row + [""] * (len(headers) - len(row))
            result.append({headers[i]: clean(padded[i]) for i in range(len(headers))})
        return result


def payload_from_admin2(row: Dict[str, str]) -> Optional[LocationPayload]:
    gov_slug = ADM1_PCODE_TO_SLUG.get(row.get("adm1_pcode", ""))
    if not gov_slug:
        return None

    adm2_en = row.get("adm2_name", "")
    adm2_ar = row.get("adm2_name1", "") or adm2_en
    pcode = row.get("adm2_pcode", "")
    slug = make_slug("district", pcode or adm2_en)

    if not adm2_en or not slug:
        return None

    return LocationPayload(
        governorate_slug=gov_slug,
        slug=slug,
        location_type="district",
        city_en=adm2_en,
        name_en="",
        city_ar=adm2_ar,
        name_ar="",
        latitude=parse_decimal(row.get("center_lat")),
        longitude=parse_decimal(row.get("center_lon")),
        is_popular=is_popular(adm2_en),
    )


def payload_from_admin3(row: Dict[str, str]) -> Optional[LocationPayload]:
    gov_slug = ADM1_PCODE_TO_SLUG.get(row.get("adm1_pcode", ""))
    if not gov_slug:
        return None

    adm2_en = row.get("adm2_name", "")
    adm2_ar = row.get("adm2_name1", "") or adm2_en
    adm3_en = row.get("adm3_name", "")
    adm3_ar = row.get("adm3_name1", "") or adm3_en
    pcode = row.get("adm3_pcode", "")
    slug = make_slug("subdistrict", pcode or adm3_en)

    if not adm3_en or not slug:
        return None

    return LocationPayload(
        governorate_slug=gov_slug,
        slug=slug,
        location_type="subdistrict",
        city_en=adm2_en or adm3_en,
        name_en=adm3_en,
        city_ar=adm2_ar or adm3_ar,
        name_ar=adm3_ar,
        latitude=parse_decimal(row.get("center_lat")),
        longitude=parse_decimal(row.get("center_lon")),
        is_popular=is_popular(adm2_en, adm3_en),
    )


def classify_populated_place(row: Dict[str, str]) -> str:
    title = clean(row.get("popplaceclasstitle", "")).lower()
    feature = clean(row.get("featurename_en", "")).lower()

    if "neighborhood" in title:
        return "area"
    if feature in POPULAR_PLACE_KEYS:
        return "city"
    return "village"


def payload_from_populated_place(row: Dict[str, str]) -> Optional[LocationPayload]:
    gov_slug = ADM1_PCODE_TO_SLUG.get(row.get("adm1_pcode", ""))
    if not gov_slug:
        return None

    feature_en = row.get("featurename_en", "") or row.get("adm4_en", "")
    feature_ar = row.get("featurename_ar", "") or row.get("adm4_ar", "") or feature_en
    adm3_en = row.get("adm3_en", "")
    adm3_ar = row.get("adm3_ar", "") or adm3_en
    adm2_en = row.get("adm2_en", "")
    adm2_ar = row.get("adm2_ar", "") or adm2_en
    pcode = row.get("pcode", "") or row.get("adm4_pcode", "")
    slug = make_slug("place", pcode or feature_en)

    if not feature_en or not slug:
        return None

    city_en = adm3_en or adm2_en or feature_en
    city_ar = adm3_ar or adm2_ar or feature_ar

    return LocationPayload(
        governorate_slug=gov_slug,
        slug=slug,
        location_type=classify_populated_place(row),
        city_en=city_en,
        name_en=feature_en,
        city_ar=city_ar,
        name_ar=feature_ar,
        latitude=parse_decimal(row.get("point_y")),
        longitude=parse_decimal(row.get("point_x")),
        is_popular=is_popular(feature_en, adm3_en, adm2_en),
    )


def payload_from_neighborhood(row: Dict[str, str]) -> Optional[LocationPayload]:
    gov_slug = ADM1_PCODE_TO_SLUG.get(row.get("adm1_pcode", ""))
    if not gov_slug:
        return None

    neighborhood_en = row.get("neighborhoodname_en", "")
    neighborhood_ar = row.get("neighborhoodname_ar", "") or neighborhood_en
    area_en = row.get("areaname_en", "")
    area_ar = row.get("areaname_ar", "") or area_en
    adm3_en = row.get("adm3_en", "")
    adm3_ar = row.get("adm3_ar", "") or adm3_en
    pcode = row.get("neighborhoodpcode", "")
    slug = make_slug("neighborhood", pcode or neighborhood_en)

    if not neighborhood_en or not slug:
        return None

    city_en = area_en or adm3_en or neighborhood_en
    city_ar = area_ar or adm3_ar or neighborhood_ar

