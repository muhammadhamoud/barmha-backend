def collect_payloads(reader: SimpleXlsxReader, include_neighborhoods: bool = True) -> List[LocationPayload]:
    payloads: List[LocationPayload] = []
    seen: set[Tuple[str, str]] = set()

    sources = [
        ("syr_admin2", payload_from_admin2),
        ("syr_admin3", payload_from_admin3),
        ("syr_populatedplaces", payload_from_populated_place),
    ]

    if include_neighborhoods:
        sources.append(("syr_neighborhoods", payload_from_neighborhood))

    for sheet_name, builder in sources:
        for row in reader.rows(sheet_name):
            payload = builder(row)
            if not payload:
                continue
            key = (payload.governorate_slug, payload.slug)
            if key in seen:
                continue
            seen.add(key)
            payloads.append(payload)

    return payloads


class Command(BaseCommand):
    help = "Seed Syria governorates, districts, subdistricts, populated places, and neighborhoods from the OCHA/HDX workbook."

    def add_arguments(self, parser):
        parser.add_argument(
            "--xlsx",
            required=True,
            help="Path to syr_admin_boundaries.xlsx.",
        )
        parser.add_argument(
            "--reset",
            action="store_true",
            help="Delete existing seeded Syria locations/governorates before seeding.",
        )
        parser.add_argument(
            "--dry-run",
            action="store_true",
            help="Read the workbook and show counts without saving changes.",
        )
        parser.add_argument(
            "--skip-neighborhoods",
            action="store_true",
            help="Skip the syr_neighborhoods sheet. Populated places are still imported.",
        )

    @transaction.atomic
    def handle(self, *args, **options):
        xlsx_path = Path(options["xlsx"])
        if not xlsx_path.exists():
            raise CommandError(f"Workbook not found: {xlsx_path}")

        with SimpleXlsxReader(xlsx_path) as reader:
            payloads = collect_payloads(
                reader,
                include_neighborhoods=not options["skip_neighborhoods"],
            )

        if options["dry_run"]:
            self.print_summary(payloads, dry_run=True)
            transaction.set_rollback(True)
            return

        if options["reset"]:
            slugs = [item["slug"] for item in GOVERNORATES]
            Location.objects.filter(governorate__slug__in=slugs).delete()
            Governorate.objects.filter(slug__in=slugs).delete()
            self.stdout.write(self.style.WARNING("Deleted existing Syria governorates and locations."))

        governorates = self.seed_governorates()

        created = 0
        updated = 0

        for payload in payloads:
            gov = governorates[payload.governorate_slug]

            location, was_created = Location.objects.get_or_create(
                governorate=gov,
                slug=payload.slug,
                defaults={
                    "location_type": payload.location_type,
                    "latitude": payload.latitude,
                    "longitude": payload.longitude,
                    "is_popular": payload.is_popular,
                },
            )

            location.location_type = payload.location_type
            location.latitude = payload.latitude
            location.longitude = payload.longitude
            location.is_popular = payload.is_popular

            location.set_current_language("en")
            location.city = payload.city_en
            location.name = payload.name_en
            location.save()

            location.set_current_language("ar")
            location.city = payload.city_ar
            location.name = payload.name_ar
            location.save()

            if was_created:
                created += 1
            else:
                updated += 1

        self.print_summary(payloads, dry_run=False, created=created, updated=updated)

    def seed_governorates(self) -> Dict[str, Governorate]:
        governorates: Dict[str, Governorate] = {}

        for item in GOVERNORATES:
            gov, _ = Governorate.objects.get_or_create(
                slug=item["slug"],
                defaults={"order": item["order"], "is_active": True},
            )

            gov.order = item["order"]
            gov.is_active = True

            gov.set_current_language("en")
            gov.name = item["en"]
            gov.save()

            gov.set_current_language("ar")
            gov.name = item["ar"]
            gov.save()

            governorates[item["slug"]] = gov

        return governorates

    def print_summary(self, payloads: List[LocationPayload], dry_run: bool, created: int = 0, updated: int = 0):
        by_type: Dict[str, int] = {}
        by_governorate: Dict[str, int] = {}

        for payload in payloads:
            by_type[payload.location_type] = by_type.get(payload.location_type, 0) + 1
            by_governorate[payload.governorate_slug] = by_governorate.get(payload.governorate_slug, 0) + 1

        self.stdout.write("\nLocation counts by type:")
        for location_type in sorted(by_type):
            self.stdout.write(f"  {location_type}: {by_type[location_type]}")

        self.stdout.write("\nLocation counts by governorate:")
        for gov in [item["slug"] for item in GOVERNORATES]:
            self.stdout.write(f"  {gov}: {by_governorate.get(gov, 0)}")

        if dry_run:
            self.stdout.write(self.style.WARNING(f"\nDry run only. Parsed locations: {len(payloads)}."))
        else:
            self.stdout.write(
                self.style.SUCCESS(
                    f"\nSeed complete. Governorates: {len(GOVERNORATES)}. "
                    f"Locations parsed: {len(payloads)}. Created: {created}. Updated: {updated}."
                )
            )
